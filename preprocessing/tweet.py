class Tweet():
    
    def __init__(self, date, time, content, fav_count, retweet_count, country_code, place_full_name, follower_count):
        self.date = date
        self.time = time
        self.content = content
        self.fav_count = fav_count
        self.retweet_count = retweet_count
        self.country_code = country_code
        self.place_full_name = place_full_name
        self.follower_count = follower_count
